#A code to tell you your age, the amount of years left for you to turn hundred years and the exact year you are going to turn hundred years
age=int(input("Enter birth year: "))
age1=2023-(age)
print("your are",age1 ,"years old")
yr=(100-(age1))
print("You have", yr, "left to turn hundred")
hundred=(yr+2023)
print("You will turn hundred in the year ",hundred)